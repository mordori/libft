#include "libft_defs.h"

float	wrapf(float a)
{
	if (a > M_PI * 2.0f)
		return (a - M_PI * 2.0f);
	else if (a < -M_PI * 2.0f)
		return (a + M_PI * 2.0f);
	return (a);
}
